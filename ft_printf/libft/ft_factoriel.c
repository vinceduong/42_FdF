int	ft_factoriel(int nb)
{
	int	res;

	res = 1;
	if (nb == 0)
		return (1);
	if (nb < 0 || nb > 12)
		return (0);
	else
	{
		while (nb > 0)
		{
			res = res * nb;
			nb--;
		}
		return (res);
	}
}
